public class TipoDeDatos {
    public static void main(String[] args) {
        int edad = 18;
        System.out.println("Mi edad es:" + edad);
        edad = 23;
        System.out.println("Mi edad correcta es:" + edad);

        boolean isActive = false;
        System.out.println("La cuenta esta activa?" + isActive);

        double sueldoBruto = 50000.50;
        System.out.println("Sueldo con inpuestos es:" + sueldoBruto); 

        float sueldoNeto = 10000;
        System.out.println("Sueldo sin inpuestos es:" + sueldoNeto); 

    }
}
